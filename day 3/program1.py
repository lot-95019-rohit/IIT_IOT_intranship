s =  "Hello IoT World"
print(s.upper())          # HELLO IOT WORLD
print(s.lower())          # hello iot world
print(s.strip())          # removes leading/trailing spaces
print(s.replace("IoT", "Python"))
print(s.split())          # split into list
print(s.find("World"))    # index position
print(s.count("o"))       # count of character
print(s.startswith("H"))
print(s.endswith("d"))